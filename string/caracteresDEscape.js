// Caracteres de escape

/* \' o \" -> permite mantener las comillas */
console.log('Hola gente \"bonita\"');

/* \n -> salto de linea */
console.log('Hola gente \n bonita');

/* \t -> tabulador */
console.log('Hola gente \t bonita');

/* \\ -> une todo */
console.log('c::windows\\windows\\carpeta\\archivo.html');